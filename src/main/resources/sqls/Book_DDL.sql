DROP TABLE IF EXISTS `pt_book`;
CREATE TABLE `pt_book` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(45) NULL COMMENT '书名',
  `author` VARCHAR(45) NULL COMMENT '作者',
  `doi` INT NOT NULL DEFAULT 0 COMMENT 'doi',
  `description` VARCHAR(128) NULL COMMENT '描述',
  `date` DATETIME NULL COMMENT '出版日期',
  `price` DOUBLE NULL COMMENT '定价',
  `created_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间戳',
  `updated_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间戳',
  PRIMARY KEY (`id`))
ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT = '测试数据类';